type EqTypeMatcher struct {
	x interface{}
}

func (e EqTypeMatcher) Matches(x interface{}) bool {
	b := utils.GetType(x)
	a := utils.GetType(e.x)
	return a == b
}

func (e EqTypeMatcher) String() string {
	return fmt.Sprintf("is equal to %v", e.x)
}

